<script>
var originalModal = $('#myModal').clone();
function edit_data(id)
{
	 $('#myModal').remove();
    var myClone = originalModal.clone();
    $('body').append(myClone);
		$.ajax({
			url : '{{ url("/get-banner")}}/' + id,
			type: "get",
			dataType: "JSON",
			success: function(data)
			{
				$('#img-travel').prepend('<img id="img-travel" src="'+data.travel_image+'" class="img-responsive" />')
				$('.modal-header').html(data.travel_title);
				$('#travel_text').html(data.travel_text);
				$('#myModal').modal('show'); 
				
			},
			error: function (jqXHR, textStatus, errorThrown)
			{
				alert('Error get data from ajax');
			}
		
		});	
	}
</script>
